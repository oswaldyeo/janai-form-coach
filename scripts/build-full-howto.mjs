// Build the complete Form Coach how-to library from reviewed research batches.
//
// Visual policy is deliberately conservative: only high-confidence matches in
// yuhonas/free-exercise-db are bundled. Medium/none matches remain text-only.
// Existing, previously verified public-domain assets are preserved.

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createHash } from 'node:crypto';
import { CATALOG_LIST } from '../js/engine/catalog.js';
import { HOWTO_BY_ID as EXISTING_HOWTO } from '../js/engine/howto.js';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const BATCHES = ['000-113', '114-227', '228-341', '342-454'];
const DB_RAW = 'https://raw.githubusercontent.com/yuhonas/free-exercise-db/main/exercises';
const DB_REPO = 'https://github.com/yuhonas/free-exercise-db';

const rows = (await Promise.all(BATCHES.map(async (range) => {
  const file = path.join(ROOT, 'research/full-catalog', `batch-${range}.json`);
  return JSON.parse(await fs.readFile(file, 'utf8'));
}))).flat();
const AUTHORED_VISUALS = JSON.parse(await fs.readFile(
  path.join(ROOT, 'assets/howto/original/manifest.json'), 'utf8',
));

validateResearch(rows);
await validateAuthoredVisuals(AUTHORED_VISUALS);

const output = {};
const sourceRows = [];
let downloaded = 0;
let textOnly = 0;

for (const row of rows) {
  const existing = EXISTING_HOWTO[row.id];
  let images = [];
  let visualSource = null;
  const authored = AUTHORED_VISUALS[row.id];

  if (authored) {
    images = [...authored.images];
    visualSource = authored.visualSource;
  } else if (row.replaceExistingVisual === true && isHighConfidenceVisual(row)) {
    ({ images, visualSource } = await materializeLibraryVisual(row));
  } else if (Array.isArray(existing?.images) && existing.images.length) {
    images = [...existing.images];
    visualSource = {
      library: 'yuhonas/free-exercise-db',
      exercise: row.sourceMatch || 'Previously verified exercise demo',
      url: DB_REPO,
      license: 'Public domain (Unlicense)',
    };
  } else if (isHighConfidenceVisual(row)) {
    ({ images, visualSource } = await materializeLibraryVisual(row));
  }

  if (!images.length) textOnly += 1;
  if (images.length) {
    sourceRows.push({
      id: row.id,
      name: row.name,
      source: visualSource.exercise,
      library: visualSource.library,
      license: visualSource.license,
      confidence: authored ? 'reviewed-original' : (row.replaceExistingVisual === true ? 'reviewed-replacement' : (existing?.images?.length ? 'verified-existing' : row.matchConfidence)),
      paths: authored?.images || row.sourceImagePaths,
    });
  }

  output[row.id] = {
    steps: row.steps.map((step) => String(step).trim()),
    cues: String(row.cues).trim(),
    images,
    guidanceSource: row.guidanceSource || null,
    visualSource: images.length ? visualSource : null,
  };
}

const moduleText = `// Generated by scripts/build-full-howto.mjs from reviewed research batches.\n`
  + `// Do not hand-edit entries; update research/full-catalog and rebuild.\n\n`
  + `export const HOWTO_BY_ID = Object.freeze(${JSON.stringify(output, null, 2)});\n\n`
  + `export function getHowto(id) {\n  return HOWTO_BY_ID[id] || null;\n}\n`;
await fs.writeFile(path.join(ROOT, 'js/engine/howto.js'), moduleText);
await fs.writeFile(path.join(ROOT, 'assets/howto/SOURCES.md'), sourceLedger(sourceRows, rows.length, textOnly));
const offlineCore = [...new Set(rows.flatMap((row) => output[row.id].images.filter(
  (src) => !src.includes('/library/') || row.replaceExistingVisual === true,
)))];
await fs.writeFile(
  path.join(ROOT, 'assets/howto/offline-core.json'),
  `${JSON.stringify(offlineCore, null, 2)}\n`,
);

console.log(JSON.stringify({ catalog: rows.length, visualExercises: sourceRows.length, textOnly, downloaded }, null, 2));

function isHighConfidenceVisual(row) {
  return row.matchConfidence === 'high' && Boolean(row.sourceMatch) && row.sourceImagePaths.length > 0;
}

async function materializeLibraryVisual(row) {
  const images = [];
  for (const [index, sourcePathRaw] of row.sourceImagePaths.slice(0, 2).entries()) {
    const sourcePath = normalizeSourcePath(sourcePathRaw);
    const digest = createHash('sha1').update(sourcePath).digest('hex').slice(0, 10);
    const ext = path.extname(sourcePath).toLowerCase() === '.png' ? '.png' : '.jpg';
    const rel = `./assets/howto/library/${safeId(row.id)}-${digest}/${index}${ext}`;
    const dest = path.join(ROOT, rel.slice(2));
    try {
      if (!(await exists(dest))) {
        const response = await fetch(`${DB_RAW}/${sourcePath}`);
        if (!response.ok) throw new Error(`HTTP ${response.status}`);
        const bytes = Buffer.from(await response.arrayBuffer());
        assertImage(bytes, ext, sourcePath);
        await fs.mkdir(path.dirname(dest), { recursive: true });
        await fs.writeFile(dest, bytes);
        downloaded += 1;
      }
      images.push(rel);
    } catch (error) {
      console.warn(`${row.id}: visual skipped (${sourcePath}: ${error.message})`);
    }
  }
  return {
    images,
    visualSource: images.length ? {
      library: 'yuhonas/free-exercise-db',
      exercise: row.sourceMatch,
      url: DB_REPO,
      license: 'Public domain (Unlicense)',
    } : null,
  };
}

function validateResearch(records) {
  if (records.length !== CATALOG_LIST.length) {
    throw new Error(`research/catalog count mismatch: ${records.length}/${CATALOG_LIST.length}`);
  }
  const ids = new Set();
  records.forEach((row, index) => {
    const catalog = CATALOG_LIST[index];
    if (row.catalogIndex !== index || row.id !== catalog.id || row.name !== catalog.name || row.equipment !== catalog.equipment) {
      throw new Error(`catalog drift at index ${index}: ${JSON.stringify({ catalog, row })}`);
    }
    if (ids.has(row.id)) throw new Error(`duplicate research id: ${row.id}`);
    ids.add(row.id);
    if (!Array.isArray(row.steps) || row.steps.length < 3 || row.steps.length > 6) {
      throw new Error(`${row.id}: expected 3-6 steps`);
    }
    if (!String(row.cues || '').trim()) throw new Error(`${row.id}: missing cues`);
    if (!['high', 'medium', 'none'].includes(row.matchConfidence)) {
      throw new Error(`${row.id}: invalid match confidence`);
    }
    if (!Array.isArray(row.sourceImagePaths)) throw new Error(`${row.id}: invalid sourceImagePaths`);
    if (row.replaceExistingVisual != null && row.replaceExistingVisual !== true) throw new Error(`${row.id}: replaceExistingVisual must be true when present`);
    if (row.replaceExistingVisual === true && (!isHighConfidenceVisual(row) || !EXISTING_HOWTO[row.id]?.images?.length)) {
      throw new Error(`${row.id}: replacement visual requires a high-confidence source and existing images`);
    }
    if (row.matchConfidence === 'high' && row.sourceMatch && !row.sourceImagePaths.length && !EXISTING_HOWTO[row.id]?.images?.length) {
      throw new Error(`${row.id}: high match has no image path`);
    }
  });
}

async function validateAuthoredVisuals(manifest) {
  for (const [id, record] of Object.entries(manifest)) {
    if (!CATALOG_LIST.some((exercise) => exercise.id === id)) throw new Error(`authored visual has unknown id: ${id}`);
    if (!Array.isArray(record.images) || record.images.length !== 2) throw new Error(`${id}: authored visual needs two frames`);
    if (!record.visualSource?.exercise || !record.visualSource?.license) throw new Error(`${id}: authored visual provenance missing`);
    for (const image of record.images) {
      if (!/^\.\/assets\/howto\/original\/[^/]+\/[01]\.svg$/.test(image)) throw new Error(`${id}: unsafe authored visual path ${image}`);
      const file = path.join(ROOT, image.slice(2));
      const markup = await fs.readFile(file, 'utf8');
      if (!markup.startsWith('<svg') || !markup.includes('<title') || !markup.includes('<desc')) throw new Error(`${id}: invalid or inaccessible SVG ${image}`);
    }
  }
}

function normalizeSourcePath(value) {
  let source = decodeURIComponent(String(value).trim()).replace(/^\.\//, '').replace(/^exercises\//, '');
  try {
    const url = new URL(source);
    const marker = '/exercises/';
    const at = url.pathname.indexOf(marker);
    if (at >= 0) source = decodeURIComponent(url.pathname.slice(at + marker.length));
  } catch {}
  if (!source || source.includes('..')) throw new Error(`unsafe source image path: ${value}`);
  return source;
}

function safeId(id) {
  return String(id).toLowerCase().replace(/[^a-z0-9-]+/g, '-').replace(/^-|-$/g, '');
}

async function exists(file) {
  try { await fs.access(file); return true; } catch { return false; }
}

function assertImage(bytes, ext, source) {
  const jpeg = bytes.length > 3 && bytes[0] === 0xff && bytes[1] === 0xd8 && bytes[2] === 0xff;
  const png = bytes.length > 8 && bytes.subarray(0, 8).equals(Buffer.from([137,80,78,71,13,10,26,10]));
  if ((ext === '.jpg' && !jpeg) || (ext === '.png' && !png)) throw new Error(`invalid image bytes: ${source}`);
}

function sourceLedger(records, catalogCount, textOnlyCount) {
  const publicCount = records.filter((record) => record.library === 'yuhonas/free-exercise-db').length;
  const originalCount = records.filter((record) => record.library === 'Form Coach original').length;
  const lines = [
    '# How-to visual sources', '',
    `Form Coach contains authored guidance for all **${catalogCount}** catalog exercises.`,
    `It includes visuals for **${records.length}** exercises: **${publicCount}** public-domain demonstrations and **${originalCount}** reviewed original movement diagrams. **${textOnlyCount}** remain text-only pending accurate original visuals.`, '',
    `Public demonstrations come from [\`yuhonas/free-exercise-db\`](${DB_REPO}) under the Unlicense. Medium-confidence and unmatched library candidates are never bundled automatically. Original SVG diagrams are deterministic Form Coach artwork retained in editable source form.`, '',
    '| Form Coach ID | Exercise | Visual source | License / confidence |',
    '|---|---|---|---|',
  ];
  for (const record of records.sort((a, b) => a.name.localeCompare(b.name))) {
    lines.push(`| \`${record.id}\` | ${escapeCell(record.name)} | ${escapeCell(`${record.source || 'Verified visual'} · ${record.library}`)} | ${escapeCell(`${record.license} · ${record.confidence}`)} |`);
  }
  lines.push('', 'Research provenance and rejected candidates are retained in `research/full-catalog/`.', '');
  return lines.join('\n');
}

function escapeCell(value) {
  return String(value).replaceAll('|', '\\|').replaceAll('\n', ' ');
}
